a, b, c = int(input("Коэффициент а: ")), int(input("Коэффициент b: ")), int(input("Коэффициент c: "))
D = b**2-4*a*c
if D == 0:
    x = -b/(2*a)
    print("Корень:", x)
elif D > 0:
    x1 = (-b+D**0.5)/(2*a)
    x2 = (-b-D**0.5)/(2*a)
    print("Первый корень:", x1, "Второй корень:", x2)
else:
    print("Корней нет")
